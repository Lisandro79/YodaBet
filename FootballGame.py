class FootballGame:
    def __init__(self, sport, match_title, league, result_to_bet, date, time_to_match,
                 best_bookie, best_odds, mean_median):
        self.sport = sport
        self.match_title = match_title
        self.league = league
        self.result_to_bet = result_to_bet
        self.date = date
        self.time_to_match = time_to_match
        self.best_bookie = best_bookie
        self.best_odds = best_odds
        self.mean_median = mean_median
